import java.util.*;
public class sum_of_two {
    public static void main(String args[]) {
        System.out.println("Enter the value of num1 and num2: ");
        Scanner sc = new Scanner(System.in);
        int num1 = sc.nextInt();
        int num2 = sc.nextInt();
        int result;
        result  = num1+num2;
        System.out.println("Sum of two number is "+result);

    }
}
